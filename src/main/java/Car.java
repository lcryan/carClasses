public class Car {
    String brandName;
    String type;
    int manufacturingYear;
    String color;
    double engineSize;

    public Car(String brandName, String type, int manufacturingYear, String color, double engineSize) {
        this.brandName = brandName;
        this.type = type;
        this.manufacturingYear = manufacturingYear;
        this.color = color;
        this.engineSize = engineSize;
    }
}
